package multilevelInheritance;

public class PokharaUniversityStudent {
	int registrationNO;
	int puSymbolNo;
	/**
	 * @return the registrationNO
	 */
	public int getRegistrationNO() {
		return registrationNO;
	}
	/**
	 * @param registrationNO the registrationNO to set
	 */
	public void setRegistrationNO(int registrationNO) {
		this.registrationNO = registrationNO;
	}
	/**
	 * @return the puSymbolNo
	 */
	public int getPuSymbolNo() {
		return puSymbolNo;
	}
	/**
	 * @param puSymbolNo the puSymbolNo to set
	 */
	public void setPuSymbolNo(int puSymbolNo) {
		this.puSymbolNo = puSymbolNo;
	}
	
	

}
